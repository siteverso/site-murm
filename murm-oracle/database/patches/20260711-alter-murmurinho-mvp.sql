SELECT table_name,
       column_name,
       data_type,
       nullable
FROM user_tab_columns
WHERE table_name IN ('MURM_USER', 'MURM_POST')
  AND column_name IN
(
    'PASSWORD_CHANGED_AT',
    'THEME_CODE',
    'DELETED_AT',
    'DELETED_BY_USER_ID',
    'MODERATED_AT',
    'MODERATION_REASON'
)
