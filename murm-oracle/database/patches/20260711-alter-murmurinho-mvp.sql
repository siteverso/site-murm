    INTO v_count
    FROM user_tables
    WHERE table_name = 'MURM_EMAIL_VERIFICATION';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE '
            CREATE TABLE murm_email_verification
            (
                id                 NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY
                                   CONSTRAINT pk_murm_email_verification PRIMARY KEY,
                user_id            NUMBER        NOT NULL,
                token_hash         VARCHAR2(500) NOT NULL,
                expires_at         TIMESTAMP     NOT NULL,
                verified_at        TIMESTAMP,
                created_at         TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

                CONSTRAINT fk_murm_email_verification_user
                    FOREIGN KEY (user_id)
                    REFERENCES murm_user (id),

                CONSTRAINT uk_murm_email_verification_token
                    UNIQUE (token_hash)
            )
        ';
    END IF;
END;
